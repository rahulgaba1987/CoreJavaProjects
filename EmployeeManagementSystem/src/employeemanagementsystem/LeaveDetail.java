    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String id = txtEmpID.getText();
        String email = txtEmail.getText();
        String contact = txtContact.getText();
        String address = txtAddress.getText();
        
        LeaveDetailBean bean = new LeaveDetailBean();
        bean.setEmpcode(Integer.parseInt(id));
        bean.setEmail(email);
        bean.setContact(email);
        bean.setAddress(address);
        
          DBOperations db = new DBOperations();
        String result =db.insertLeaveRecord(bean);
        if(result.equals("valid"))
        {
            JOptionPane.showMessageDialog(null, "Employee Leave Detail is inserted");
        }
        
     
        
        
    }//GEN-LAST:event_btnSaveActionPerformed

