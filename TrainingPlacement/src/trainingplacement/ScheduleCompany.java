    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        
        String scheduleDate=txtScheduleDate.getText();
       String criteria= txtCompanyCriteria.getText();
       String academicyear= txtAcademicYear.getText();
        String minSalry=txtMinSalary.getText();
        String companyID=txtCompanyID.getText();
        String tpoId=txtTPOID.getText();
        
        CompanyScheduleBean bean = new CompanyScheduleBean();
        bean.setScheduleDate(scheduleDate);
        bean.setCriteria(criteria);
        bean.setAcademicyear(academicyear);
        bean.setMin_salary(minSalry);
        bean.setComp_id(companyID);
        bean.setTpo_id(tpoId);
        
        DBOperations obj = new DBOperations();
        String result=obj.insertCompanySchedule(bean);
        if(result.equals("valid"))
        {
            JOptionPane.showMessageDialog(null,"Company schedule is inserted ");
            clear();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

