    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String sql = "INSERT INTO `employee`(`ID`, `Department`, `Name`, `Phone`, `Email`, `National_ID`, `Qualification`, `Join_Date`, `Shift`, `Salary`, `Experience`, `Date_of_Birth`, `Gender`, `Person`) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtEmpID.getText());
            pst.setString(2, (String) ddlAccount.getSelectedItem());
            pst.setString(3, txtName.getText());
            pst.setString(4, txtPhone.getText());
            pst.setString(5, txtEmail.getText());
            pst.setString(6, txtNationalID.getText());
            pst.setString(7, txtQualification.getText());
       
          
      
            pst.setString(8, txtJoiningDate.getText());
            pst.setString(9, (String) ddlShift.getSelectedItem());
            pst.setString(10, txtSalary.getText());
            pst.setString(11, (String) ddlExperience.getSelectedItem());
            pst.setString(12, ddlDOB.getText());
            pst.setString(13, (String) ddlGender.getSelectedItem());
           // String d1= (String) ddlGender.;
            
          
            pst.setString(14, jTextField7.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "New Employee Added...!!");
        } catch(Exception e){
            System.out.println("Exception is occurred in Add Employee Detail "+e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

