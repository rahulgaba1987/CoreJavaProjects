    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
       String username= txtUsername.getText();
       String password =txtPassword.getText();
       
       LoginBean objBean = new LoginBean();
       objBean.setUsername(username);
       objBean.setPassword(password);
       
        CommonServices objServices = new CommonServices();
        String result=objServices.checkLogin(objBean);
        
        if(result.equals("valid"))
        {
            this.dispose();
            MainFrame objFrame = new MainFrame();
        }
        else
        {
            lblWarning.setForeground(Color.red);
            lblWarning.setText("User or Password is not correct");
        }
       
       
        
        
    }//GEN-LAST:event_btnLoginActionPerformed

